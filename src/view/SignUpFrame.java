    private void tfUserCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfUserCaretUpdate
        String user = tfUser.getText();
        if (user.length() < 4) {
            lbCheckUser.setText(translator.translate("signup.username.invalid1"));
            lbCheckUser.setForeground(java.awt.Color.RED);
        } else if (!user.matches("^[a-zA-Z0-9_]+$")) {
            lbCheckUser.setText(translator.translate("signup.username.invalid2"));
            lbCheckUser.setForeground(java.awt.Color.RED);
        } else {
            lbCheckUser.setText(translator.translate("signup.username.valid"));
            lbCheckUser.setForeground(java.awt.Color.GREEN);
            isUserOK = true;
        }
        if (Authentication.userExists(user)) {
            lbCheckUser.setText(translator.translate("signup.username.invalid3"));
            lbCheckUser.setForeground(java.awt.Color.RED);
            isUserOK = false;
        }
    }//GEN-LAST:event_tfUserCaretUpdate

    private void btnShowHideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowHideActionPerformed
        isShowPass = !isShowPass;
        if (isShowPass) {
            btnShowHide.setText(translator.translate("btn.hide.shortcut"));
            btnShowHide.setToolTipText(translator.translate("btn.hide"));
            tfPass.setEchoChar((char) 0);
        } else {
            btnShowHide.setText(translator.translate("btn.show.shortcut"));
            btnShowHide.setToolTipText(translator.translate("btn.show"));
            tfPass.setEchoChar('*');
        }
    }//GEN-LAST:event_btnShowHideActionPerformed

    private void tfPassCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfPassCaretUpdate
        String pass = String.valueOf(tfPass.getPassword());
        if (pass.length() < 8) {
            lbCheckPass1.setText(translator.translate("signup.password.invalid1"));
            lbCheckPass1.setForeground(java.awt.Color.RED);
            lbCheckPass2.setText("");
        } else if (!pass.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]+$")) {
            lbCheckPass1.setText(translator.translate("signup.password.invalid2"));
            lbCheckPass1.setForeground(java.awt.Color.RED);
            lbCheckPass2.setText(translator.translate("signup.password.invalid3"));
            lbCheckPass2.setForeground(java.awt.Color.RED);
        } else {
            lbCheckPass1.setText(translator.translate("signup.password.valid"));
            lbCheckPass1.setForeground(java.awt.Color.GREEN);
            lbCheckPass2.setText("");
            isPassOK = true;
        }
    }//GEN-LAST:event_tfPassCaretUpdate


    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        String user = tfUser.getText().trim();
        String pass = String.valueOf(tfPass.getPassword()).trim();
        // String firstName = tfFirstName.getText().trim();
        // String lastName = tfLastName.getText().trim();
        // String dob = (dateChooser.getDate() != null) ?
        //         new java.text.SimpleDateFormat("yyyy-MM-dd").format(dateChooser.getDate()) : "";
        // String phone = tfPhone.getText().trim();
        String email = tfEmail.getText().trim();
        String role = "user";
        if (!isUserOK || !isPassOK) {
            JOptionPane.showMessageDialog(this, translator.translate("signup.error.invalid"), 
                    translator.translate("signup.error.title"), JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (Authentication.register(user, pass, email, role)) {
            JOptionPane.showMessageDialog(this, translator.translate("signup.success"), 
                    translator.translate("signup.success.title"), JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, translator.translate("signup.username.invalid3"), 
                    translator.translate("signup.error.title"), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

